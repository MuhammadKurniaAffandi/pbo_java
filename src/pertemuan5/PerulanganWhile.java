package pertemuan5;

public class PerulanganWhile {
    public static void main(String[] args) {
        int H = 1;

        while (H <= 5) {
            System.out.println("Perulangan pakai WHILE ke- : " + H);
            System.out.println("Saya suka sekali belajar java");
            System.out.println("Di Labkom UBL");
            System.out.println("  ");
            H++;
        }
    }
}
