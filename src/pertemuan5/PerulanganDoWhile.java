package pertemuan5;

public class PerulanganDoWhile {
    public static void main(String[] args) {
        int R = 1;
        do {
            System.out.println("Perulangan pakai DO-WHILE ke- : " + R);
            System.out.println("Saya suka sekali belajar java");
            System.out.println("Di Labkom UBL");
            System.out.println("  ");
            R++;
        } while (R <= 5);

    }
}
