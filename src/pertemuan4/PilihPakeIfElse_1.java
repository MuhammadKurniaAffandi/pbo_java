package pertemuan4;

public class PilihPakeIfElse_1 {
    public static void main(String[] args) {
        int T = 4, R = 9;

        // kondisi if ... else ... dengan 1 baris statement,
        // tidak perlu menggunakan simbol {}
        if (T < R)
            System.out.println("T lebih kecil dari R");
        else
            System.out.println("T lebih besar dari R");
    }
}
